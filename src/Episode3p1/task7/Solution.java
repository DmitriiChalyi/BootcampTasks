package Episode3p1.task7;

import java.io.*;
import java.util.*;

public class Solution {
    public static void main(String[] args) throws Exception {
        Scanner scanner = new Scanner(System.in);
        int i = scanner.nextInt();
        while (i > 0) {
            System.out.println("I'm not a traitor");
            i--;
        }
    }
}
