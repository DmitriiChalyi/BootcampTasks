package Episode7p1.task4;

public class Solution {
	public static void main(String[] args) {
	}
	public static int maxSword(int a, int b){
		return Math.max(a, b);
	}
	public static double maxSword(double a, double b){
		return Math.max(a, b);
	}


}

/*
Нужно написать public static методы: int maxSword(int, int), long maxSword(long, long), double maxSword(double, double).
Каждый метод должен возвращать максимальную длину светового меча из двух переданных в него длин светового меча.
 */